// ex:-
var a = 4;
switch(a){
    case 1:
        console.log("One");
        break;
    case 2: 
        console.log("two");
        break;
    case 3: 
        console.log("three");
        break;
    case 4: 
        console.log("four");
        break;
    case 5: 
        console.log("five");
        break;
    default: 
        console.log("default");
}
//output:-four

