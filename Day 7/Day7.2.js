// Date Formats😍😍
/*
Type	    Example
ISO Date	"2010-08-05" (The International Standard)
Short Date	"05/05/2008"
Long Date	"Jun 25 2015" or "25 Mar 2015"
*/


var date = new Date("2010-08-05");
console.log(date);//output:-Thu Aug 05 2010 05:30:00 GMT+0530 (India Standard Time)


var date = new Date("05/08/2008");
console.log(date);//output:-Thu May 08 2008 00:00:00 GMT+0530 (India Standard Time)
